class Piloto {

  private String nombre;



// constructor
	public Piloto(String nombre) {

		this.nombre = nombre;
	}


	/**
	* Returns value of nombre
	* @return
	*/
	public String getNombre() {
		return nombre;
	}

	/**
	* Sets new value of nombre
	* @param
	*/
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
}
